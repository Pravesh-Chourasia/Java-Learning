
public class FinalConcept {

	public static void main(String[] args) {
		// Final Keyword concept //final keyword is used to define constant variable 
		//Final keyword is used to prevent inheritance 
		//Final keyword is used to prevent method overloading
		final int a = 6;  //Here we are using final keyword to define constant variable 
		a =7;
		a =8;
		System.out.println(a);
		

	}

}
