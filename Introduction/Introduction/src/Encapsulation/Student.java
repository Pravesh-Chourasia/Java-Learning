package Encapsulation;

public class Student {
	
  private String name; //Declare variables 
  private int age; //Declare variables 

public void setName(String newName) {
	name = newName;
}

public String getName() {
	return name;
}


public void setAge(int newAge) {
	age = newAge;
}

public int getAge() {
	return age;
}
}

