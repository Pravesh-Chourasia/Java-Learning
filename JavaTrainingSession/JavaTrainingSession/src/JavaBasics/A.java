package JavaBasics;

public class A {

	public A() {
		System.out.println("Parent Class Constructor");
		
	}
	public A(int i) {
		System.out.println("Value of i is " +i);
		
	}
	public A(int a, int b) {
		System.out.println("Value of a is " +a );
		System.out.println("Value of b is " +b);
		
	}
	}


